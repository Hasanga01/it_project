    private void displaysubjectdetailsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_displaysubjectdetailsMouseClicked
        // TODO add your handling code here:
        
        //for do the delete and update part
        int r = displaysubjectdetails.getSelectedRow();
        
        String id = displaysubjectdetails.getValueAt(r, 0).toString();
        String year = displaysubjectdetails.getValueAt(r, 1).toString();
        String semester = displaysubjectdetails.getValueAt(r, 2).toString();
        String subjectname = displaysubjectdetails.getValueAt(r, 3).toString();
        String subjectcode = displaysubjectdetails.getValueAt(r, 4).toString();
        String lhours = displaysubjectdetails.getValueAt(r, 5).toString();
        String labhours = displaysubjectdetails.getValueAt(r, 6).toString();
        String thours = displaysubjectdetails.getValueAt(r, 7).toString();
        String ehours = displaysubjectdetails.getValueAt(r, 8).toString();
        
        offeredyearbox.setSelectedItem(year);
        semesterbox.setSelectedItem(semester);
        snamebox.setText(subjectname);
        scodebox.setText(subjectcode);
        idbox.setText(id);
        lhoursbox.setValue(Integer.valueOf(lhours));
        labhoursbox.setValue(Integer.valueOf(labhours));
        thoursbox.setValue(Integer.valueOf(thours));
        ehoursbox.setValue(Integer.valueOf(ehours));
    }//GEN-LAST:event_displaysubjectdetailsMouseClicked

