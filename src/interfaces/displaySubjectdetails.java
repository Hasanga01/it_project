    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        //update button
        
        int x = JOptionPane.showConfirmDialog(null, "Do you really want to update!!");
        
        if(x ==0)
        {
             try
            {
            String id = idbox.getText();
            String year = offeredyearbox.getSelectedItem().toString();
            String semester = semesterbox.getSelectedItem().toString();
            String subjectname = snamebox.getText();
            String subjectcode = scodebox.getText();
            String lhours = lhoursbox.getValue().toString();
            String labhours  = labhoursbox.getValue().toString();
            String thours  = thoursbox.getValue().toString();
            String ehours  = ehoursbox.getValue().toString();
            
            
           
            String sql = "UPDATE sub_details SET offeredyr = '"+ year +"',offeredsem = '"+ semester +"',subname = '"+ subjectname +"',subcode = '"+ subjectcode +"', lecturehrs = '"+ lhours +"', labhrs = '"+ labhours +"',tutehrs = '"+ thours +"',evahrs = '"+ ehours +"' where id = '"+ id +"' ";
            
           
                pst = con.prepareStatement(sql);
                pst.execute();
                
                JOptionPane.showMessageDialog(null,"Data Successfully Updated!");
                
                //load table
                
                tableload();
            
            }catch (HeadlessException | SQLException e){
                
                JOptionPane.showMessageDialog(this, e.getMessage());
                
            }
        
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

